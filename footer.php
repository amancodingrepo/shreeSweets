<script>
// Location Modal Logic
const modal = document.getElementById('location-modal');
const modalContent = document.getElementById('location-modal-content');

// Load stored location
document.addEventListener('DOMContentLoaded', () => {
    const stored = localStorage.getItem('shree_delivery_location');
    const headerLocations = document.querySelectorAll('#header-location-text');
    if (stored) {
        headerLocations.forEach(el => el.textContent = stored);
    }
});

function openLocationModal() {
    modal.classList.remove('hidden');
    modal.classList.add('flex');
    setTimeout(() => {
        modal.classList.remove('opacity-0');
        modal.classList.add('opacity-100');
        modalContent.classList.remove('scale-95');
        modalContent.classList.add('scale-100');
        document.getElementById('location-input').focus();
    }, 10);
}

function closeLocationModal() {
    modal.classList.remove('opacity-100');
    modal.classList.add('opacity-0');
    modalContent.classList.remove('scale-100');
    modalContent.classList.add('scale-95');
    setTimeout(() => {
        modal.classList.add('hidden');
        modal.classList.remove('flex');
    }, 300);
}

modal.addEventListener('click', function(e) {
    if (e.target === modal) closeLocationModal();
});

function saveLocation(event) {
    event.preventDefault();
    let val = document.getElementById('location-input').value.trim();
    if (val) {
        // truncate if too long
        if (val.length > 15) {
            val = val.substring(0, 15) + '...';
        }
        val = val.charAt(0).toUpperCase() + val.slice(1);
        localStorage.setItem('shree_delivery_location', val);
        const headerLocations = document.querySelectorAll('#header-location-text');
        headerLocations.forEach(el => el.textContent = val);
        closeLocationModal();
    }
}
</script>

